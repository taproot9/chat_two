<template>
    <div class="is-pulled-right">
        <i class="fa fa-circle green" v-if="checkUser"/>
        <i class="fa fa-circle red" v-else/>
    </div>
</template>

<script>
    export default {
        props: [
            'friend',
            'onlineusers'
        ],
        methods: {
            checkUser : function () {
                return _.find(this.onlineusers, {id: this.friend.id});
            }
        }
    }
</script>

<style >
    .red{
        color: red;
    }
    .green{
        color: green;
    }
</style>
